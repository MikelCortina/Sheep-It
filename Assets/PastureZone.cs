using System.Collections.Generic;
using UnityEngine;

public class PastureZone : MonoBehaviour
{
    [Header("Pasture Config")]
    [Tooltip("Lista de celdas que pertenecen a esta pradera (se rellena con el pincel)")]
    public List<GrassCell> cells = new List<GrassCell>();

    [Header("Painter Ś Grass Prefabs")]
    public GameObject[] grassPrefabs;

    [Header("Query Settings")]
    [Tooltip("Cußntas celdas candidatas se eval·an por b·squeda (performance vs calidad)")]
    public int maxCandidatesPerQuery = 12;

    // ?????????????????????????????????????????????????????????????
    // Registro estßtico de todas las zonas activas en escena
    // ?????????????????????????????????????????????????????????????
    private static List<PastureZone> _allZones = new List<PastureZone>();

    public static List<PastureZone> AllZones
    {
        get { return _allZones; }
    }

    void OnEnable()
    {
        if (!_allZones.Contains(this))
            _allZones.Add(this);
    }

    void OnDisable()
    {
        _allZones.Remove(this);
    }

    // ?????????????????????????????????????????????????????????????
    // Devuelve la mejor celda pasteable cercana a una posici¾n dada
    // ?????????????????????????????????????????????????????????????
    public GrassCell FindBestCell(Vector3 origin, float searchRadius)
    {
        GrassCell best = null;
        float bestScore = -1f;
        int candidateCount = 0;

        foreach (GrassCell cell in cells)
        {
            if (cell == null || !cell.IsGrazeable) continue;

            float d = Vector3.Distance(origin, cell.transform.position);
            if (d > searchRadius) continue;

            float score = cell.grassAmount / Mathf.Max(d, 0.5f);
            if (score > bestScore)
            {
                bestScore = score;
                best = cell;
            }

            candidateCount++;
            if (candidateCount >= maxCandidatesPerQuery) break;
        }

        return best;
    }

    // ?????????????????????????????????????????????????????????????
    void OnDrawGizmosSelected()
    {
        Gizmos.color = new Color(0.1f, 0.8f, 0.1f, 0.15f);
        foreach (GrassCell cell in cells)
        {
            if (cell != null)
                Gizmos.DrawSphere(cell.transform.position, 0.5f);
        }
    }
}